public final class CGPoint {

    private double x = 0.0;
    private double y = 0.0;

    public CGPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }

}
