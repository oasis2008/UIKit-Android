public final class CGSize {

    private double mWidth = 0.0;
    private double mHeight = 0.0;

    public CGSize(double width, double height) {
        this.mWidth = width;
        this.mHeight = height;
    }

    public double getWidth() {
        return mWidth;
    }

    public double getHeight() {
        return mHeight;
    }

}
