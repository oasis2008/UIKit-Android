public final class CGSize {

    private double width = 0.0;
    private double height = 0.0;

    public CGSize(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public double getHeight() {
        return height;
    }

}
